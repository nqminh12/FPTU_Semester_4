    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        double salary = Double.parseDouble(request.getParameter("salary"));
        String dated = request.getParameter("date");
        String[] word = dated.trim().split("[/]");
        String result = word[2]+"-"+word[0]+"-"+word[1];

        Date date = Date.valueOf(result);
        boolean active = request.getParameter("active").equals("yes");
        Job job = new Job(0,name, salary, date, active);
        JobDBcontext jdb = new JobDBcontext();
        jdb.insertJob(job);
        request.setAttribute("job", job);
        request.setAttribute("dated", dated);

        request.getRequestDispatcher("AddJob.jsp").forward(request, response);

    }

