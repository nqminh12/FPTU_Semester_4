package model;

import java.sql.Date;

/**
 *
 * @author Admin
 */
public class Employee {
    public int id;
    public String name;
    public Date date;
    public boolean gender;

    public Employee() {
    }

    public Employee(int id, String name, Date date, boolean gender) {
        this.id = id;
        this.name = name;
        this.date = date;
        this.gender = gender;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }
    
}
